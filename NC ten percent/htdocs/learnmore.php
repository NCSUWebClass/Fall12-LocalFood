	<h1>Learn More</h1>
	<br />
    <p class="introtext">
    In 2007, the Center for Environmental Farming Systems (CEFS) and its many partners sought to ask — and answer — the question:<br /><br />
<em>Can we build a sustainable local food economy, from farm to fork?</em> Through the enthusiastic support of thousands of North Carolinians and hundreds of local, regional and statewide organizations, the clear answer is: YES!
    </p><br /><br />
    
    <img src="img/learnmore-1.jpg" />
    <br /><br /><br />
    
	<p class="subhead">
    <a name="cefs">CEFS</a> 
    </p>
    <div id="leftcolumn">
    <p>
    For a big-picture perspective, review <a href="http://www.cefs.ncsu.edu/resources/stateactionguide2010.pdf" target="_blank">CEFS' From Farm to Fork: Building North Carolina's Local Sustainable Food Economy</a>. This statewide action guide identifies goals and the strategies needed to move North Carolina forward.
<br /><br />
The 10% Campaign is one way you can support this statewide initiative!
    </p>
    </div>
    
    <div id="rightcolumn">
    <p>
    The <a href="http://www.cefs.ncsu.edu" target="_blank">CEFS website</a> is a great starting point for learning more. CEFS has a comprehensive directory of local foods activities throughout the state: <a href="http://ncfoodnet.com" target="_blank">NC Food NETwork</a>. You'll also find a how-to guide for setting up a company-based <a href="http://www.cefs.ncsu.edu/resources/workplacecsabulletin.html" target="_blank">multifarm CSA</a>, find a research-based organic <a href="http://www.cefs.ncsu.edu/resources/guides/organicproductionguide.html" target="_blank">production manual</a> or learn more about what we do <a href="http://www.cefs.ncsu.edu/whatwedo.html" target="_blank">here</a>.</p>
   </div>
   
    
    <img src="img/learnmore-2.jpg" style="padding-top:20px;padding-bottom:20px;" />
    
    <p class="subhead">
    <a name="ncce">N.C. Cooperative Extension</a> 
    </p>
    
    <div id="leftcolumn">
    <p>
   	<a href="http://www.ces.ncsu.edu" target="_blank">North Carolina Cooperative Extension</a> (NCCES) has been providing university-based consumer education, information and research to the public since the early 1900s. It's a great resource for a variety of topics, including:
</p><br />
<p>
<a href="http://www.ces.ncsu.edu" target="_blank">Growing Small Farms</a>: An extensive resource for farmers and gardeners alike, this website offers information for producers and consumers, including a comprehensive <a href="http://www.ces.ncsu.edu/chatham/ag/SustAg/buylocalguide.html" target="_blank">"Buy Local Guide"</a> and useful information on many things from plant disease to organic pest management.
    </p>
    </div>
    
    <div id="rightcolumn">
    <p>
   An NCSU program, <a href="http://plantsforhumanhealth.ncsu.edu/extension/overview/" target="_blank">NC Market Ready</a> is part of the College of Agriculture and Life Sciences and Cooperative Extension. Farmers and retailers can find important food safety information on its website. The site also features <a href="http://www.theproducelady.org" target="_blank">"The Produce Lady,"</a> food expert Brenda Sutton, with useful videos, a blog and recipes on how to cook, prepare and preserve some of North Carolina's best produce.
</p><br />
<p>
Food safety, nutritional information and an entire spectrum of health and human development topics are found through NCCES' <a href="http://www.ces.ncsu.edu/depts/fcs" target="_blank">Family and Consumer Sciences</a> website.
   	</p>
   </div>
   
    
    <img src="img/learnmore-3.jpg" style="padding-top:20px;padding-bottom:20px;" />
    
    <p class="subhead">
    <a name="additional">Additional Resources</a> 
    </p>
    
    <div id="leftcolumn">
    <p>
   	<a href="http://www.myeatsmartmovemore.com" target="_blank">Eat Smart Move More</a>: A great source for healthy lifestyle information geared to educate North Carolinians on eating smarter and active living.
</p><br />
<p>
<a href="http://www.hpdp.unc.edu/research/sustainable-agriculture" target="_blank">The University of North Carolina's Center for Health Promotion and Disease Prevention</a>: Addresses pressing food-related health problems like obesity and diabetes, and connects solutions to local food systems.
</p><br />
<p>
North Carolina boasts nine local conviviums (chapters) of <a href="http://www.slowfoodusa.org/index.php/local_chapters/#North%20Carolina" target="_blank">Slow Food USA</a>, supporting "good, clean and fair food." Many of the state's Slow Food chapters have local food events and offer directories to restaurants that feature locally sourced food.
</p><br />
<p>
<a href="http://www.ncpanbranch.com/" target="_blank">North Carolina Department of Health and Human Services' Physical Activity and Nutrition (PAN) Branch</a>: PAN works statewide to promote increased opportunities for physical activity and healthy eating through policy and environmental change.
    </p>
    </div>
    
    <div id="rightcolumn">
    <p>
   Want to start a community garden or find one in your area? <a href="http://nccommunitygarden.ncsu.edu/" target="_blank">North Carolina Community Gardens</a> has just what you're looking for.
</p><br />
<p>
The <a href="http://www.ncagr.gov/" target="_blank">North Carolina Department of Agriculture and Consumer Services (NCDA&amp;CS)</a> website provides many important resources relevant to local foods. Check <a href="http://www.ncagr.gov/htm/programs.htm" target="_blank">here</a> for statewide agriculture statistics, consumer programs and guides.
</p><br />
<p>
Visit <a href="http://www.feastdowneast.org/" target="_blank">Southeastern North Carolina Food Systems Project</a>, a leader in the local food movement for the southeastern part of the state. Look for information on its food policy council, local foods assessment and local food sourcing guides.
</p><br />
<p>
Are you interested in specific North Carolina commodities? Find <a href="http://www.ncstrawberry.org" target="_blank">strawberries</a>, <a href="http://www.ncsweetpotatoes.com" target="_blank">sweet potatoes</a> and more on this <a href="http://harvest.cals.ncsu.edu/dean/index.cfm?pageID=1096" target="_blank">NC State College of Agriculture and Life Sciences Commodities Leaders Directory</a>.
   	</p>
   </div>
   
    
    <img src="img/learnmore-4.jpg" style="padding-top:20px;padding-bottom:20px;" />
    
    
    
    <div id="leftcolumn">
    <p>
   	<a href="http://www.asapconnections.org/" target="_blank">Appalachian Sustainable Agriculture Project (ASAP)</a>: Western North Carolina's "go-to" organization for finding local food and connecting with the farmers who grow it and the markets, grocers, and restaurants committed to using locally grown products. ASAP is also the organization behind Appalachian Grown™, a certification program for farm products grown or raised in Western North Carolina and the Southern Appalachian mountains.
</p><br />
<p>
The <a href="http://www.ee.enr.state.nc.us" target="_blank">North Carolina Office of Environmental Education</a> is a wonderful resource for statewide environmental education. The office serves preK-12 schools, colleges and universities, government agencies, nonprofit organizations and more. It has an online searchable directory and also highlights local foods projects and organizations in its environmental education network.
</p><br />
<p>
<a href="http://www.ncseagrant.org" target="_blank">North Carolina Sea Grant</a> is a university collaborative, nonprofit organization focused on research, education and outreach opportunities relating to current issues affecting the North Carolina coast and its communities, including local seafood market development and fisheries resources.
    </p>
    </div>
    
    <div id="rightcolumn">
    <p>
   The <a href="http://www.rafiusa.org" target="_blank">Rural Advancement Foundation International (RAFI–USA)</a> is active in policy development around sustainable agriculture issues, including labeling and contract reform. Its <a href="http://www.rafiusa.org/programs/cttt.html" target="_blank">Come to the Table</a> project explores the connections between food security, faith and farms. Read its new publication, <a href="http://www.cometothetablenc.org/guide.pdf" target="_blank">Come to the Table: How People of Faith Can Relieve Hunger and Sustain Local Farms in North Carolina</a>, for more information on faith-based food and health initiatives in North Carolina.
</p><br />
<p>
Check out <a href="http://www.carolinafarmstewards.org" target="_blank">Carolina Farm Stewardship Association (CFSA)</a> for annual farm tours, to receive sustainable agriculture policy alerts, and to find a local food directory and links to area farms. CFSA hosts an annual sustainable agriculture conference that is open to the public.
</p><br />
<p>
Learn about how to save money and still eat healthy, local food at <a href="http://www.cookforgood.com/save-money/" target="_blank">Cook for Good</a>.
</p><br />
<p>
Many or our other partner organizations have wonderful resources available relevant to building a local food economy. Check out their websites through links to <a href="partners.php">our partners</a> page.
   	</p>
